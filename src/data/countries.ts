export const searchCountries = (query: string): Country[] => {
  const lowercaseQuery = query.toLowerCase();
  return countries.filter(country => 
    country.name.toLowerCase().includes(lowercaseQuery) ||
    country.code.toLowerCase().includes(lowercaseQuery) ||
    country.region.toLowerCase().includes(lowercaseQuery) ||
    country.languages.some(lang => lang.toLowerCase().includes(lowercaseQuery))
  );
};

// Pays proches géographiquement (même région)
export const getNearbyCountries = (countryCode: string): Country[] => {
  const country = getCountryByCode(countryCode);
  if (!country) return [];
  
  return countries.filter(c => 
    c.region === country.region && c.code !== country.code
  );
};

// Pays avec consulats d'urgence disponibles
export const getCountriesWithConsulates = (): Country[] => {
  return countries.filter(country => 
    country.consulateContact && 
    country.consulateContact !== 'N/A' && 
    !country.consulateContact.startsWith('+33')
  );
};

export const getLanguageByCode = (code: string): Language | undefined => {
  return localLanguages.find(lang => lang.code === code);
};

export const getCommonLanguages = (): Language[] => {
  return localLanguages.filter(lang => lang.isCommon);
};

export const getProblemTypeById = (id: string): ProblemType | undefined => {
  return problemTypes.find(type => type.id === id);
};

export const getProblemTypesByCategory = (category: string): ProblemType[] => {
  return problemTypes.filter(type => type.category === category);
};

export const getCriticalProblems = (): ProblemType[] => {
  return problemTypes.filter(type => type.urgency === 'critical');
};

export const getProblemTypesByExpert = (expertType: string): ProblemType[] => {
  return problemTypes.filter(type => type.expertType === expertType);
};

export const getProblemTypesByUrgency = (urgency: string): ProblemType[] => {
  return problemTypes.filter(type => type.urgency === urgency);
};

