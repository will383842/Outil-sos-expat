export const getCountryStats = () => {
  const stats = {
    totalCountries: countries.length,
    byRegion: {} as Record<string, number>,
    byRiskLevel: {} as Record<string, number>,
    byCurrency: {} as Record<string, number>,
    withConsulates: 0,
    frenchSpeaking: 0
  };

  countries.forEach(country => {
    // Par région
    stats.byRegion[country.region] = (stats.byRegion[country.region] || 0) + 1;
    
    // Par niveau de risque
    stats.byRiskLevel[country.riskLevel] = (stats.byRiskLevel[country.riskLevel] || 0) + 1;
    
    // Par devise
    stats.byCurrency[country.currency] = (stats.byCurrency[country.currency] || 0) + 1;
    
    // Avec consulats
    if (country.consulateContact && country.consulateContact !== 'N/A') {
      stats.withConsulates++;
    }
    
    // Francophone
    if (country.languages.includes('Français')) {
      stats.frenchSpeaking++;
    }
  });

  return stats;
};

export const getProblemTypeStats = () => {
  const stats = {
    totalTypes: problemTypes.length,
    byCategory: {} as Record<string, number>,
    byUrgency: {} as Record<string, number>,
    byExpertType: {} as Record<string, number>,
    averageDuration: 0,
    withDocuments: 0
  };

  let totalDuration = 0;
  
  problemTypes.forEach(type => {
    stats.byCategory[type.category] = (stats.byCategory[type.category] || 0) + 1;
    stats.byUrgency[type.urgency] = (stats.byUrgency[type.urgency] || 0) + 1;
    stats.byExpertType[type.expertType] = (stats.byExpertType[type.expertType] || 0) + 1;
    totalDuration += type.estimatedDuration;
    
    if (type.requiredDocuments && type.requiredDocuments.length > 0) {
      stats.withDocuments++;
    }
  });

  stats.averageDuration = Math.round(totalDuration / problemTypes.length);

  return stats;
};

